package model;

class Category {

	private Integer categoruId;
	private String name;

	public Category() {
	}

	public Integer getCategoruId() {
		return categoruId;
	}

	public void setCategoruId(Integer categoruId) {
		this.categoruId = categoruId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
